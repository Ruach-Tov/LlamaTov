%% CUPTI activity buffer callbacks (called by CUPTI runtime)
emit_cupti_callbacks(S) :-
    format(S, '; --- CUPTI buffer callbacks (called by CUPTI runtime) ---~n', []),
    %% bufferRequested: allocate 32KB buffer
    format(S, 'define void @cupti_buffer_requested(ptr %buf_out, ptr %size_out, ptr %max_out) {~n', []),
    format(S, '  %buf = call ptr @malloc(i64 32768)~n', []),
    format(S, '  store ptr %buf, ptr %buf_out~n', []),
    format(S, '  store i64 32768, ptr %size_out~n', []),
    format(S, '  store i64 0, ptr %max_out~n', []),
    format(S, '  ret void~n', []),
    format(S, '}~n~n', []),
    %% bufferCompleted: walk records, accumulate stalls
    format(S, 'define void @cupti_buffer_completed(ptr %ctx, i32 %streamId, ptr %buffer, i64 %size, i64 %validSize) {~n', []),
    format(S, 'entry:~n', []),
    format(S, '  %record_ptr = alloca ptr~n', []),
    format(S, '  store ptr null, ptr %record_ptr~n', []),
    format(S, '  br label %loop~n', []),
    format(S, '~n', []),
    format(S, 'loop:~n', []),
    format(S, '  %status = call i32 @cuptiActivityGetNextRecord(ptr %buffer, i64 %validSize, ptr %record_ptr)~n', []),
    format(S, '  %done = icmp ne i32 %status, 0~n', []),
    format(S, '  br i1 %done, label %cleanup, label %process~n', []),
    format(S, '~n', []),
    format(S, 'process:~n', []),
    format(S, '  %record = load ptr, ptr %record_ptr~n', []),
    %% Check activity kind: offset 0 is the kind field (i32)
    %% CUPTI_ACTIVITY_KIND_PC_SAMPLING = 51
    format(S, '  %kind_ptr = getelementptr i8, ptr %record, i32 0~n', []),
    format(S, '  %kind = load i32, ptr %kind_ptr~n', []),
    format(S, '  %is_pc = icmp eq i32 %kind, 51~n', []),
    format(S, '  br i1 %is_pc, label %accumulate, label %loop~n', []),
    format(S, '~n', []),
    format(S, 'accumulate:~n', []),
    %% CUpti_ActivityPCSampling3 layout:
    %%   offset 0: kind (i32), offset 4: flags (i32)
    %%   offset 8: sourceLocatorId (i32), offset 12: correlationId (i32)
    %%   offset 16: functionId (i32), offset 20: pcOffset (i32)
    %%   offset 24: stallReason (i32), offset 28: samples (i32)
    format(S, '  %stall_ptr = getelementptr i8, ptr %record, i32 24~n', []),
    format(S, '  %stall_reason = load i32, ptr %stall_ptr~n', []),
    format(S, '  %samples_ptr = getelementptr i8, ptr %record, i32 28~n', []),
    format(S, '  %samples = load i32, ptr %samples_ptr~n', []),
    format(S, '  %samples64 = zext i32 %samples to i64~n', []),
    %% Clamp stall_reason to 0..11
    format(S, '  %in_range = icmp ult i32 %stall_reason, 12~n', []),
    format(S, '  %idx = select i1 %in_range, i32 %stall_reason, i32 11~n', []),
    %% Accumulate into @cupti_stalls[idx]
    format(S, '  %slot_ptr = getelementptr [13 x i64], ptr @cupti_stalls, i32 0, i32 %idx~n', []),
    format(S, '  %old = load i64, ptr %slot_ptr~n', []),
    format(S, '  %new = add i64 %old, %samples64~n', []),
    format(S, '  store i64 %new, ptr %slot_ptr~n', []),
    %% Also accumulate total (slot 12)
    format(S, '  %total_ptr = getelementptr [13 x i64], ptr @cupti_stalls, i32 0, i32 12~n', []),
    format(S, '  %old_total = load i64, ptr %total_ptr~n', []),
    format(S, '  %new_total = add i64 %old_total, %samples64~n', []),
    format(S, '  store i64 %new_total, ptr %total_ptr~n', []),
    format(S, '  br label %loop~n', []),
    format(S, '~n', []),
    format(S, 'cleanup:~n', []),
    format(S, '  call void @free(ptr %buffer)~n', []),
    format(S, '  ret void~n', []),
    format(S, '}~n~n', []).

