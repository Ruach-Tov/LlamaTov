%% nvml_bridge_emitter.pl — Generate LLVM IR for the NVML-to-SWI-Prolog bridge.
%%
%% Emits a .ll file that, when compiled with llc + clang, produces a .so
%% that bridges SWI-Prolog's FLI to NVML ioctls.
%%
%% The generated code calls NVML functions (dlopen/dlsym at runtime)
%% and exposes them as Prolog predicates:
%%
%%   gpu_temperature(-Temp)     — reads PTHERM register
%%   gpu_power(-MilliWatts)     — reads power sensor
%%   gpu_clock(-SmMHz, -MemMHz) — reads PCLOCK registers
%%   gpu_memory(-TotalMB, -UsedMB, -FreeMB) — reads PFB
%%   gpu_utilization(-GpuPct, -MemPct) — reads PCOUNTER activity
%%   gpu_pcie(-Gen, -Width)     — reads PBUS config
%%   gpu_name(-Name)            — reads PMC identification
%%
%% Usage:
%%   swipl -g 'emit_nvml_bridge("nvml_bridge.ll"), halt' nvml_bridge_emitter.pl
%%   llc -relocation-model=pic -filetype=obj nvml_bridge.ll -o nvml_bridge.o
%%   clang -shared -o nvml_bridge.so nvml_bridge.o -ldl
%%   swipl -g 'use_foreign_library(nvml_bridge), gpu_temperature(T), write(T), nl, halt'
%%
%% Author: mavchin (2026-06-01)

:- module(nvml_bridge_emitter, [emit_nvml_bridge/1, emit_gpu_bridge/1, emit_full_bridge/1]).

emit_nvml_bridge(OutFile) :-
    open(OutFile, write, S),
    emit_header(S),
    emit_types(S),
    emit_global_state(S),
    emit_helper_functions(S),
    emit_nvml_init(S),
    emit_gpu_temperature(S),
    emit_gpu_power(S),
    emit_gpu_clock(S),
    emit_gpu_memory(S),
    emit_gpu_utilization(S),
    emit_gpu_pcie(S),
    emit_gpu_name(S),
    emit_install_function(S),
    close(S),
    format("Emitted NVML bridge IR to ~w~n", [OutFile]).

%% ============================================================
%% LLVM IR header
%% ============================================================

emit_header(S) :-
    format(S, '; nvml_bridge.ll — Generated by nvml_bridge_emitter.pl~n', []),
    format(S, '; Bridges SWI-Prolog FLI to NVML ioctls for GPU register access.~n', []),
    format(S, '; Path: Prolog query → FLI → this .so → NVML → ioctl → kernel → MMIO → hardware~n', []),
    format(S, '~n', []),
    format(S, 'target datalayout = "e-m:e-p270:32:32-p271:32:32-p272:64:64-i64:64-i128:128-f80:128-n8:16:32:64-S128"~n', []),
    format(S, 'target triple = "x86_64-unknown-linux-gnu"~n', []),
    format(S, '~n', []).

%% ============================================================
%% Type declarations
%% ============================================================

emit_types(S) :-
    format(S, '; --- External function declarations ---~n', []),
    format(S, 'declare ptr @dlopen(ptr, i32)~n', []),
    format(S, 'declare ptr @dlsym(ptr, ptr)~n', []),
    format(S, 'declare i32 @printf(ptr, ...)~n', []),
    format(S, '~n', []),
    %% SWI-Prolog FLI functions
    format(S, '; SWI-Prolog FLI~n', []),
    format(S, 'declare i64 @PL_new_term_ref()~n', []),
    format(S, 'declare i32 @PL_unify_integer(i64, i64)~n', []),
    format(S, 'declare i32 @PL_unify_atom_chars(i64, ptr)~n', []),
    format(S, 'declare i32 @PL_get_integer(i64, ptr)~n', []),
    format(S, 'declare i32 @PL_register_foreign(ptr, i32, ptr, i32)~n', []),
    format(S, '~n', []).

%% ============================================================
%% Global state (NVML handle, device handle, function pointers)
%% ============================================================

emit_global_state(S) :-
    format(S, '; --- Global state ---~n', []),
    format(S, '@nvml_lib = internal global ptr null~n', []),
    format(S, '@nvml_dev = internal global ptr null~n', []),
    format(S, '@nvml_initialized = internal global i32 0~n', []),
    format(S, '~n', []),
    %% Function pointer globals for each NVML function we use
    format(S, '@fn_init = internal global ptr null~n', []),
    format(S, '@fn_shutdown = internal global ptr null~n', []),
    format(S, '@fn_gethandle = internal global ptr null~n', []),
    format(S, '@fn_getname = internal global ptr null~n', []),
    format(S, '@fn_gettemp = internal global ptr null~n', []),
    format(S, '@fn_getpower = internal global ptr null~n', []),
    format(S, '@fn_getclock = internal global ptr null~n', []),
    format(S, '@fn_getmeminfo = internal global ptr null~n', []),
    format(S, '@fn_getutil = internal global ptr null~n', []),
    format(S, '@fn_getpciegeneration = internal global ptr null~n', []),
    format(S, '@fn_getpciewidth = internal global ptr null~n', []),
    format(S, '~n', []),
    %% String constants
    emit_string_constant(S, 'str_libname', 'libnvidia-ml.so.1'),
    emit_string_constant(S, 'str_init', 'nvmlInit_v2'),
    emit_string_constant(S, 'str_shutdown', 'nvmlShutdown'),
    emit_string_constant(S, 'str_gethandle', 'nvmlDeviceGetHandleByIndex_v2'),
    emit_string_constant(S, 'str_getname', 'nvmlDeviceGetName'),
    emit_string_constant(S, 'str_gettemp', 'nvmlDeviceGetTemperature'),
    emit_string_constant(S, 'str_getpower', 'nvmlDeviceGetPowerUsage'),
    emit_string_constant(S, 'str_getclock', 'nvmlDeviceGetClockInfo'),
    emit_string_constant(S, 'str_getmeminfo', 'nvmlDeviceGetMemoryInfo'),
    emit_string_constant(S, 'str_getutil', 'nvmlDeviceGetUtilizationRates'),
    emit_string_constant(S, 'str_getpciegeneration', 'nvmlDeviceGetCurrPcieLinkGeneration'),
    emit_string_constant(S, 'str_getpciewidth', 'nvmlDeviceGetCurrPcieLinkWidth'),
    emit_string_constant(S, 'str_pred_temp', 'gpu_temperature'),
    emit_string_constant(S, 'str_pred_power', 'gpu_power'),
    emit_string_constant(S, 'str_pred_clock', 'gpu_clock'),
    emit_string_constant(S, 'str_pred_memory', 'gpu_memory'),
    emit_string_constant(S, 'str_pred_util', 'gpu_utilization'),
    emit_string_constant(S, 'str_pred_pcie', 'gpu_pcie'),
    emit_string_constant(S, 'str_pred_name', 'gpu_name'),
    format(S, '~n', []).

emit_string_constant(S, Name, Value) :-
    atom_length(Value, Len),
    Len1 is Len + 1,
    format(S, '@~w = private constant [~w x i8] c"~w\\00"~n', [Name, Len1, Value]).

%% ============================================================
%% Helper: ensure_nvml_init — lazy init on first call
%% ============================================================

emit_helper_functions(S) :-
    format(S, '; --- resolve_sym: dlsym wrapper ---~n', []),
    format(S, 'define internal ptr @resolve_sym(ptr %lib, ptr %name) {~n', []),
    format(S, '  %fn = call ptr @dlsym(ptr %lib, ptr %name)~n', []),
    format(S, '  ret ptr %fn~n', []),
    format(S, '}~n~n', []).

emit_nvml_init(S) :-
    format(S, '; --- ensure_nvml_init: lazy dlopen + dlsym + nvmlInit ---~n', []),
    format(S, 'define internal i32 @ensure_nvml_init() {~n', []),
    format(S, 'entry:~n', []),
    format(S, '  %already = load i32, ptr @nvml_initialized~n', []),
    format(S, '  %done = icmp ne i32 %already, 0~n', []),
    format(S, '  br i1 %done, label %ret_ok, label %do_init~n', []),
    format(S, '~n', []),
    format(S, 'do_init:~n', []),
    %% dlopen
    format(S, '  %lib = call ptr @dlopen(ptr @str_libname, i32 2)~n', []),
    format(S, '  %lib_null = icmp eq ptr %lib, null~n', []),
    format(S, '  br i1 %lib_null, label %ret_fail, label %resolve~n', []),
    format(S, '~n', []),
    format(S, 'resolve:~n', []),
    format(S, '  store ptr %lib, ptr @nvml_lib~n', []),
    %% Resolve all function pointers
    emit_resolve_fn(S, 'fn_init', 'str_init'),
    emit_resolve_fn(S, 'fn_shutdown', 'str_shutdown'),
    emit_resolve_fn(S, 'fn_gethandle', 'str_gethandle'),
    emit_resolve_fn(S, 'fn_getname', 'str_getname'),
    emit_resolve_fn(S, 'fn_gettemp', 'str_gettemp'),
    emit_resolve_fn(S, 'fn_getpower', 'str_getpower'),
    emit_resolve_fn(S, 'fn_getclock', 'str_getclock'),
    emit_resolve_fn(S, 'fn_getmeminfo', 'str_getmeminfo'),
    emit_resolve_fn(S, 'fn_getutil', 'str_getutil'),
    emit_resolve_fn(S, 'fn_getpciegeneration', 'str_getpciegeneration'),
    emit_resolve_fn(S, 'fn_getpciewidth', 'str_getpciewidth'),
    format(S, '~n', []),
    %% Call nvmlInit
    format(S, '  %init_fn = load ptr, ptr @fn_init~n', []),
    format(S, '  %init_ret = call i32 %init_fn()~n', []),
    format(S, '  %init_fail = icmp ne i32 %init_ret, 0~n', []),
    format(S, '  br i1 %init_fail, label %ret_fail, label %get_device~n', []),
    format(S, '~n', []),
    %% Get device handle
    format(S, 'get_device:~n', []),
    format(S, '  %devptr = alloca ptr~n', []),
    format(S, '  %gh_fn = load ptr, ptr @fn_gethandle~n', []),
    format(S, '  %gh_ret = call i32 %gh_fn(i32 0, ptr %devptr)~n', []),
    format(S, '  %dev = load ptr, ptr %devptr~n', []),
    format(S, '  store ptr %dev, ptr @nvml_dev~n', []),
    format(S, '  store i32 1, ptr @nvml_initialized~n', []),
    format(S, '  br label %ret_ok~n', []),
    format(S, '~n', []),
    format(S, 'ret_ok:~n', []),
    format(S, '  ret i32 0~n', []),
    format(S, '~n', []),
    format(S, 'ret_fail:~n', []),
    format(S, '  ret i32 1~n', []),
    format(S, '}~n~n', []).

emit_resolve_fn(S, Global, StrName) :-
    format(S, '  %~w_ptr = call ptr @resolve_sym(ptr %lib, ptr @~w)~n', [Global, StrName]),
    format(S, '  store ptr %~w_ptr, ptr @~w~n', [Global, Global]).

