%% CPU PMU counter definitions
%% Each counter: {name, perf_type, config, fd_global}
pmu_counter(cycles,              0, 0,      'fd_cycles').       % PERF_TYPE_HARDWARE, PERF_COUNT_HW_CPU_CYCLES
pmu_counter(instructions,        0, 1,      'fd_insns').        % PERF_COUNT_HW_INSTRUCTIONS
pmu_counter(cache_misses,        0, 3,      'fd_cache_miss').   % PERF_COUNT_HW_CACHE_MISSES
pmu_counter(branch_misses,       0, 5,      'fd_branch_miss').  % PERF_COUNT_HW_BRANCH_MISSES
pmu_counter(sse_packed_single,   4, 16400,  'fd_sse_ps').       % PERF_TYPE_RAW, 0x4010
pmu_counter(sse_scalar_single,   4, 8208,   'fd_sse_ss').       % PERF_TYPE_RAW, 0x2010
pmu_counter(avx_256_packed,      4, 273,    'fd_avx_ps').       % PERF_TYPE_RAW, 0x0111

emit_pmu_globals(S) :-
    format(S, '; --- CPU PMU globals ---~n', []),
    forall(pmu_counter(_, _, _, FdGlobal),
           format(S, '@~w = internal global i32 -1~n', [FdGlobal])),
    format(S, '@pmu_started = internal global i32 0~n', []),
    format(S, '~n', []),
    %% perf_event_attr struct: 112 bytes on x86_64
    %% We only need: type(offset 0, i32), size(offset 4, i32), config(offset 8, i64),
    %%               disabled(offset 40 bit 0), exclude_kernel(offset 40 bit 1), exclude_hv(offset 40 bit 2)
    %% String constants for counter names
    forall(pmu_counter(Name, _, _, _),
           (format(atom(StrGlobal), 'str_pmu_~w', [Name]),
            emit_string_constant(S, StrGlobal, Name))),
    emit_string_constant(S, 'str_pred_pmu_start', 'cpu_pmu_start'),
    emit_string_constant(S, 'str_pred_pmu_stop', 'cpu_pmu_stop'),
    emit_string_constant(S, 'str_pred_pmu_read', 'cpu_pmu_read'),
    format(S, '~n', []).

%% cpu_pmu_start/0 — open perf_event_open for each counter, enable them
emit_pmu_start(S) :-
    format(S, '; --- cpu_pmu_start/0: open + enable PMU counters ---~n', []),
    format(S, 'define i32 @pl_pmu_start(i64 %t0, i32 %arity, ptr %control) {~n', []),
    format(S, 'entry:~n', []),
    %% Allocate perf_event_attr (112 bytes, zeroed)
    format(S, '  %attr = alloca [112 x i8]~n', []),
    format(S, '  call ptr @memset(ptr %attr, i32 0, i64 112)~n', []),
    %% Set size field (offset 4) = 112
    format(S, '  %size_ptr = getelementptr i8, ptr %attr, i32 4~n', []),
    format(S, '  store i32 112, ptr %size_ptr~n', []),
    %% Set disabled=1, exclude_kernel=1, exclude_hv=1 (offset 40, bits 0,1,2 = 0x7)
    format(S, '  %flags_ptr = getelementptr i8, ptr %attr, i32 40~n', []),
    format(S, '  store i64 7, ptr %flags_ptr~n', []),
    format(S, '~n', []),
    %% Open each counter via syscall 298 (perf_event_open)
    findall(counter(Name, Type, Config, FdGlobal), pmu_counter(Name, Type, Config, FdGlobal), Counters),
    emit_pmu_open_counters(S, Counters, 0),
    format(S, '~n', []),
    %% Enable all counters (ioctl fd, PERF_EVENT_IOC_ENABLE=0x2400, 0)
    emit_pmu_enable_all(S, Counters),
    format(S, '  store i32 1, ptr @pmu_started~n', []),
    format(S, '  ret i32 1~n', []),
    format(S, '}~n~n', []).

emit_pmu_open_counters(_, [], _).
emit_pmu_open_counters(S, [counter(_Name, Type, Config, FdGlobal)|Rest], Idx) :-
    format(atom(TypeLabel), 'type~w', [Idx]),
    format(atom(CfgLabel), 'cfg~w', [Idx]),
    format(atom(FdLabel), 'fd~w', [Idx]),
    %% Set type (offset 0)
    format(S, '  ; counter ~w~n', [_Name]),
    format(S, '  %~w_p = getelementptr i8, ptr %attr, i32 0~n', [TypeLabel]),
    format(S, '  store i32 ~w, ptr %~w_p~n', [Type, TypeLabel]),
    %% Set config (offset 8)
    format(S, '  %~w_p = getelementptr i8, ptr %attr, i32 8~n', [CfgLabel]),
    format(S, '  store i64 ~w, ptr %~w_p~n', [Config, CfgLabel]),
    %% syscall 298 = perf_event_open(attr, pid=0, cpu=-1, group=-1, flags=0)
    format(S, '  %~w_64 = call i64 (i64, ...) @syscall(i64 298, ptr %attr, i32 0, i32 -1, i32 -1, i64 0)~n', [FdLabel]),
    format(S, '  %~w = trunc i64 %~w_64 to i32~n', [FdLabel, FdLabel]),
    format(S, '  store i32 %~w, ptr @~w~n', [FdLabel, FdGlobal]),
    Idx1 is Idx + 1,
    emit_pmu_open_counters(S, Rest, Idx1).

emit_pmu_enable_all(_, []).
emit_pmu_enable_all(S, [counter(_, _, _, FdGlobal)|Rest]) :-
    format(atom(FdLoad), 'en_~w', [FdGlobal]),
    format(S, '  %~w = load i32, ptr @~w~n', [FdLoad, FdGlobal]),
    %% PERF_EVENT_IOC_RESET = 0x2403, PERF_EVENT_IOC_ENABLE = 0x2400
    format(S, '  call i32 (i32, i64, ...) @ioctl(i32 %~w, i64 9219, i32 0)~n', [FdLoad]),  % 0x2403 = reset
    format(S, '  call i32 (i32, i64, ...) @ioctl(i32 %~w, i64 9216, i32 0)~n', [FdLoad]),  % 0x2400 = enable
    emit_pmu_enable_all(S, Rest).

%% cpu_pmu_stop/0 — disable and close all counters
emit_pmu_stop(S) :-
    format(S, '; --- cpu_pmu_stop/0: disable + close PMU counters ---~n', []),
    format(S, 'define i32 @pl_pmu_stop(i64 %t0, i32 %arity, ptr %control) {~n', []),
    format(S, 'entry:~n', []),
    findall(FdGlobal, pmu_counter(_, _, _, FdGlobal), Fds),
    emit_pmu_close_all(S, Fds),
    format(S, '  store i32 0, ptr @pmu_started~n', []),
    format(S, '  ret i32 1~n', []),
    format(S, '}~n~n', []).

emit_pmu_close_all(_, []).
emit_pmu_close_all(S, [FdGlobal|Rest]) :-
    format(atom(FdLoad), 'cl_~w', [FdGlobal]),
    format(S, '  %~w = load i32, ptr @~w~n', [FdLoad, FdGlobal]),
    format(S, '  call i32 (i32, i64, ...) @ioctl(i32 %~w, i64 9217, i32 0)~n', [FdLoad]),  % 0x2401 = disable
    format(S, '  call i32 @close(i32 %~w)~n', [FdLoad]),
    emit_pmu_close_all(S, Rest).

%% cpu_pmu_read(-List) — read all counters, return as Name-Value pairs
emit_pmu_read(S) :-
    format(S, '; --- cpu_pmu_read(List): read all PMU counters ---~n', []),
    format(S, 'define i32 @pl_pmu_read(i64 %t0, i32 %arity, ptr %control) {~n', []),
    format(S, 'entry:~n', []),
    format(S, '  %list = call i64 @PL_copy_term_ref(i64 %t0)~n', []),
    format(S, '  %head = call i64 @PL_new_term_ref()~n', []),
    format(S, '  %dash_atom = call i64 @PL_new_atom(ptr @str_dash)~n', []),
    format(S, '  %dash_fn = call i64 @PL_new_functor(i64 %dash_atom, i32 2)~n', []),
    format(S, '  %val_buf = alloca i64~n', []),
    format(S, '~n', []),
    findall(counter(Name, _, _, FdGlobal), pmu_counter(Name, _, _, FdGlobal), Counters),
    emit_pmu_read_counters(S, Counters, 0),
    format(S, '  %ok_nil = call i32 @PL_unify_nil(i64 %list)~n', []),
    format(S, '  ret i32 %ok_nil~n', []),
    format(S, '}~n~n', []).

emit_pmu_read_counters(_, [], _).
emit_pmu_read_counters(S, [counter(Name, _, _, FdGlobal)|Rest], Idx) :-
    format(atom(FdLoad), 'rfd~w', [Idx]),
    format(atom(KeyRef), 'rkey~w', [Idx]),
    format(atom(ValRef), 'rval~w', [Idx]),
    format(atom(PairRef), 'rpair~w', [Idx]),
    format(atom(StrGlobal), 'str_pmu_~w', [Name]),
    format(S, '  ; read ~w~n', [Name]),
    format(S, '  %~w = load i32, ptr @~w~n', [FdLoad, FdGlobal]),
    format(S, '  call i64 @read(i32 %~w, ptr %val_buf, i64 8)~n', [FdLoad]),
    format(S, '  %~w_raw = load i64, ptr %val_buf~n', [ValRef]),
    format(S, '  %~w = call i64 @PL_new_term_ref()~n', [KeyRef]),
    format(S, '  %~w = call i64 @PL_new_term_ref()~n', [ValRef]),
    format(S, '  %~w = call i64 @PL_new_term_ref()~n', [PairRef]),
    format(S, '  call i32 @PL_put_atom_chars(i64 %~w, ptr @~w)~n', [KeyRef, StrGlobal]),
    format(S, '  call i32 @PL_unify_integer(i64 %~w, i64 %~w_raw)~n', [ValRef, ValRef]),
    format(S, '  call i32 @PL_cons_functor(i64 %~w, i64 %dash_fn, i64 %~w, i64 %~w)~n', [PairRef, KeyRef, ValRef]),
    format(S, '  call i32 @PL_unify_list(i64 %list, i64 %head, i64 %list)~n', []),
    format(S, '  call i32 @PL_unify(i64 %head, i64 %~w)~n', [PairRef]),
    format(S, '~n', []),
    Idx1 is Idx + 1,
    emit_pmu_read_counters(S, Rest, Idx1).

