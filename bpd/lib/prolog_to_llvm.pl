%! emit_kernel(+Name, +OutputFile) is det.
%  Emit LLVM IR for a kernel to a file.
emit_kernel(Name, OutputFile) :-
    emit_kernel(Name, OutputFile, scalar).

%! emit_kernel(+Name, +OutputFile, +Mode) is det.
%  Mode: scalar | vectorized
emit_kernel(Name, OutputFile, Mode) :-
    open(OutputFile, write, S),
    emit_header(S, Name),
    (Mode == vectorized ->
        emit_vectorized_body(S, Name)
    ;
        emit_scalar_body(S, Name)
    ),
    emit_footer(S),
    close(S),
    format("Emitted ~w to ~w (~w mode)~n", [Name, OutputFile, Mode]).

%% Header: target triple, datalayout, function declaration
emit_header(S, Name) :-
    get_target(triple, Triple),
    kernel_op(Name, elementwise, Inputs, _),
    length(Inputs, NInputs),
    % Function name
    kernel_fn_name(Name, FnName),
    format(S, '; LLVM IR generated by prolog_to_llvm.pl~n', []),
    format(S, '; Kernel: ~w~n', [Name]),
    format(S, '; Mode: BPD substrate, bit-identical emission~n~n', []),
    format(S, 'target triple = "~w"~n~n', [Triple]),
    % Function signature: void @bpd_add_llvm(float* %a, float* %b, float* %x, i32 %n)
    (NInputs =:= 2 ->
        format(S, 'define void @~w(float* noalias %a, float* noalias %b, float* noalias %x, i32 %n) {~n', [FnName])
    ;
        format(S, 'define void @~w(float* noalias %a, float* noalias %x, i32 %n) {~n', [FnName])
    ).

kernel_fn_name(k_add, "bpd_add_llvm").
kernel_fn_name(k_mul, "bpd_mul_llvm").
kernel_fn_name(k_sub, "bpd_sub_llvm").
kernel_fn_name(k_relu, "bpd_relu_llvm").
kernel_fn_name(k_neg, "bpd_neg_llvm").
kernel_fn_name(k_abs, "bpd_abs_llvm").

%% Scalar loop body for binary elementwise ops (add, mul, sub)
emit_scalar_body(S, Name) :-
    kernel_op(Name, elementwise, [_, _], _),
    elementwise_llvm_op(Name, LLVMOp), !,
    format(S, 'entry:~n', []),
    format(S, '  br label %loop.header~n~n', []),
    format(S, 'loop.header:~n', []),
    format(S, '  %i = phi i32 [ 0, %entry ], [ %i.next, %loop.body ]~n', []),
    format(S, '  %cmp = icmp slt i32 %i, %n~n', []),
    format(S, '  br i1 %cmp, label %loop.body, label %exit~n~n', []),
    format(S, 'loop.body:~n', []),
    format(S, '  %idx = sext i32 %i to i64~n', []),
    format(S, '  %a.ptr = getelementptr float, float* %a, i64 %idx~n', []),
    format(S, '  %b.ptr = getelementptr float, float* %b, i64 %idx~n', []),
    format(S, '  %x.ptr = getelementptr float, float* %x, i64 %idx~n', []),
    format(S, '  %a.val = load float, float* %a.ptr, align 4~n', []),
    format(S, '  %b.val = load float, float* %b.ptr, align 4~n', []),
    format(S, '  %result = ~w float %a.val, %b.val~n', [LLVMOp]),
    format(S, '  store float %result, float* %x.ptr, align 4~n', []),
    format(S, '  %i.next = add i32 %i, 1~n', []),
    format(S, '  br label %loop.header~n~n', []).

%% Scalar body for relu: x = max(a, 0)
emit_scalar_body(S, k_relu) :- !,
    format(S, 'entry:~n', []),
    format(S, '  br label %loop.header~n~n', []),
    format(S, 'loop.header:~n', []),
    format(S, '  %i = phi i32 [ 0, %entry ], [ %i.next, %loop.body ]~n', []),
    format(S, '  %cmp = icmp slt i32 %i, %n~n', []),
    format(S, '  br i1 %cmp, label %loop.body, label %exit~n~n', []),
    format(S, 'loop.body:~n', []),
    format(S, '  %idx = sext i32 %i to i64~n', []),
    format(S, '  %a.ptr = getelementptr float, float* %a, i64 %idx~n', []),
    format(S, '  %x.ptr = getelementptr float, float* %x, i64 %idx~n', []),
    format(S, '  %a.val = load float, float* %a.ptr, align 4~n', []),
    format(S, '  %zero = fadd float 0.0, 0.0~n', []),
    format(S, '  %is.pos = fcmp ogt float %a.val, %zero~n', []),
    format(S, '  %result = select i1 %is.pos, float %a.val, float 0.0~n', []),
    format(S, '  store float %result, float* %x.ptr, align 4~n', []),
    format(S, '  %i.next = add i32 %i, 1~n', []),
    format(S, '  br label %loop.header~n~n', []).

